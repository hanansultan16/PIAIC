#Basic Printing
print("Hello World")
print("Hanan")
#Printing Multiple Items
print("Hanan Sultan")
print(1, 2, 3)
#Printing Special Characters
print("Hello\nWorld")
print("Hello\tWorld")
#Using the sep Parameter
print("Cricket", "Football", "Hockey", sep=", ")
print(1, 2, 3, sep="-")
#Using the end Parameter
print("Hello", "World", end=" ")
print(1, 2, end=" ")
#Escape Characters
print("He said, \"Hello!\"")
print("This is backslash: \\")
#Combining Text and Numbers
age = 20
print("I am", age, "years old")
number = 7
print("The number is", number)
#Basic Loop for Printing
print(1)
print(2)
print(3)
print(4)
print(5)


